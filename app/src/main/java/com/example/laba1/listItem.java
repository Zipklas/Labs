package com.example.laba1;

public class listItem {
     String nameBook;
     String nameAuthor;


    public listItem(String nameBook, String nameAuthor) {
        this.nameBook = nameBook;
        this.nameAuthor = nameAuthor;
    }

    public String getNameBook() {
        return nameBook;
    }

    public String getNameAuthor() {
        return nameAuthor;
    }


}